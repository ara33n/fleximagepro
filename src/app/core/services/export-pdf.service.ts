@Injectable({ providedIn: 'root' })
export class ExportPdfService {
  rows(title: string, rows: ExportPdfRow[]): Uint8Array {
    const pages: string[] = [];
    let commands = this.pageHeader(title, 1);
    let pageNumber = 1;
    let y = 662;

    for (const row of rows) {
      const labelLines = this.wrap(row.label, 24);
      const valueLines = this.wrap(row.value, 62);
      const height = Math.max(38, Math.max(labelLines.length, valueLines.length) * 12 + 18);
      if (y - height < 58) {
        commands += this.footer(pageNumber);
        pages.push(commands);
        pageNumber += 1;
        commands = this.pageHeader(title, pageNumber);
        y = 662;
      }
      commands += this.rowBox(y, height, labelLines, valueLines);
      y -= height + 8;
    }

    commands += this.footer(pageNumber);
    pages.push(commands);
    return this.buildPdf(pages);
  }

  text(title: string, text: string, rows: ExportPdfRow[] = []): Uint8Array {
    const metaRows = rows.length ? rows : [{ label: 'Output', value: text }];
    return this.rows(title, metaRows);
  }

  private pageHeader(title: string, page: number): string {
    const now = new Date().toLocaleString();
    return [
      'q',
      '0.97 0.98 0.98 rg 0 0 612 792 re f',
      '1 1 1 rg 32 704 548 56 re f',
      '0.82 0.86 0.86 RG 0.8 w 32 704 548 56 re S',
      'q 32 0 0 34 44 715 cm /Logo Do Q',
      this.textAt('FlexImagePro', 86, 735, 17, 'F2', '0.05 0.08 0.10'),
      this.textAt('Free online tools report', 86, 719, 9, 'F1', '0.39 0.39 0.42'),
      this.textAt(now, 398, 733, 8, 'F1', '0.39 0.39 0.42'),
      this.textAt(`Page ${page}`, 398, 719, 8, 'F1', '0.39 0.39 0.42'),
      '1 1 1 rg 32 674 548 22 re f',
      '0.05 0.58 0.53 RG 1 w 32 674 548 22 re S',
      this.textAt(title, 44, 681, 11, 'F2', '0.05 0.08 0.10'),
      'Q',
    ].join('\n') + '\n';
  }

  private rowBox(y: number, height: number, labelLines: string[], valueLines: string[]): string {
    const commands = [
      'q',
      '1 1 1 rg 40 ' + (y - height) + ` 532 ${height} re f`,
      '0.86 0.88 0.90 RG 0.7 w 40 ' + (y - height) + ` 532 ${height} re S`,
      '0.94 0.98 0.98 rg 40 ' + (y - height) + ` 156 ${height} re f`,
      '0.86 0.88 0.90 RG 0.7 w 196 ' + (y - height) + ` 0 ${height} re S`,
    ];
    let labelY = y - 17;
    for (const line of labelLines) {
      commands.push(this.textAt(line, 52, labelY, 8.5, 'F2', '0.16 0.19 0.22'));
      labelY -= 12;
    }
    let valueY = y - 17;
    for (const line of valueLines) {
      commands.push(this.textAt(line, 210, valueY, 8.5, 'F1', '0.12 0.12 0.14'));
      valueY -= 12;
    }
    commands.push('Q');
    return commands.join('\n') + '\n';
  }

  private footer(page: number): string {
    return [
      'q',
      '0.82 0.86 0.86 RG 0.6 w 40 38 532 0 re S',
      this.textAt('Generated by FlexImagePro', 40, 24, 8, 'F1', '0.45 0.45 0.48'),
      this.textAt(`Page ${page}`, 536, 24, 8, 'F1', '0.45 0.45 0.48'),
      'Q',
    ].join('\n') + '\n';
  }

  private buildPdf(pageStreams: string[]): Uint8Array {
    const font1 = 3 + pageStreams.length * 2;
    const font2 = font1 + 1;
    const logoObject = font2 + 1;
    const kids = pageStreams.map((_, index) => `${3 + index * 2} 0 R`).join(' ');
    const objects: string[] = [
      '1 0 obj << /Type /Catalog /Pages 2 0 R >> endobj',
      `2 0 obj << /Type /Pages /Kids [${kids}] /Count ${pageStreams.length} >> endobj`,
    ];

    pageStreams.forEach((stream, index) => {
      const pageObject = 3 + index * 2;
      const contentObject = pageObject + 1;
      objects.push(`${pageObject} 0 obj << /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Resources << /Font << /F1 ${font1} 0 R /F2 ${font2} 0 R >> /XObject << /Logo ${logoObject} 0 R >> >> /Contents ${contentObject} 0 R >> endobj`);
      objects.push(`${contentObject} 0 obj << /Length ${stream.length} >> stream\n${stream}\nendstream endobj`);
    });

    objects.push(`${font1} 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica >> endobj`);
    objects.push(`${font2} 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >> endobj`);
    objects.push(`${logoObject} 0 obj << /Type /XObject /Subtype /Image /Width ${LOGO_WIDTH} /Height ${LOGO_HEIGHT} /ColorSpace /DeviceRGB /BitsPerComponent 8 /Filter /ASCIIHexDecode /Length ${LOGO_RGB_HEX.length + 1} >> stream\n${LOGO_RGB_HEX}>\nendstream endobj`);

    let pdf = '%PDF-1.4\n';
    const offsets = [0];
    for (const object of objects) {
      offsets.push(pdf.length);
      pdf += `${object}\n`;
    }
    const xref = pdf.length;
    pdf += `xref\n0 ${objects.length + 1}\n0000000000 65535 f \n`;
    for (let i = 1; i <= objects.length; i++) {
      pdf += `${String(offsets[i]).padStart(10, '0')} 00000 n \n`;
    }
    pdf += `trailer << /Size ${objects.length + 1} /Root 1 0 R >>\nstartxref\n${xref}\n%%EOF`;
    return new TextEncoder().encode(pdf);
  }

  private textAt(value: string, x: number, y: number, size: number, font: 'F1' | 'F2', color: string): string {
    return `${color} rg BT /${font} ${size} Tf ${x} ${y} Td (${this.pdfText(value)}) Tj ET`;
  }

  private wrap(value: string, size: number): string[] {
    const words = String(value || '').replace(/\s+/g, ' ').trim().split(' ');
    const lines: string[] = [];
    let current = '';
    for (const word of words) {
      if (!current) {
        current = word;
      } else if (`${current} ${word}`.length <= size) {
        current = `${current} ${word}`;
      } else {
        lines.push(...this.breakLong(current, size));
        current = word;
      }
    }
    if (current) lines.push(...this.breakLong(current, size));
    return lines.length ? lines : [''];
  }

  private breakLong(value: string, size: number): string[] {
    if (value.length <= size) return [value];
    const lines: string[] = [];
    for (let index = 0; index < value.length; index += size) {
      lines.push(value.slice(index, index + size));
    }
    return lines;
  }

  private pdfText(value: string): string {
    return String(value).replace(/[^\x20-\x7e]/g, ' ').replace(/([\\()])/g, '\\$1');
  }
}
